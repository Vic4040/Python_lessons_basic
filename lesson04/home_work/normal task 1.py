def split_by_upper_case_non_re(s):
    a = []
    r = -1
    for i, b in enumerate(s):
        if b.islower():
            if r == -1:
                r = i
        elif b.isupper():
            if r != -1:
                a.append(s[r:i])
                r = -1
        else:
            r = -1
    if r != -1:
        a.append(s[r:])
    return a



